TCHAR GetChar(TCHAR _char)
{
	if (_char < 32)
		return '.';

	return _char;
}

char *DumpMem(unsigned char *pAddr, int len)
{
	memset(buf, 0, 16384);

	char temp[256];
	BYTE fChar;
	int i=0;
	long bytesRead=0;
	TCHAR line[17];
	int nSpaces=3*16+2;
	memset(&line, 0, sizeof(line));
	int dataidx=0;

	if (dblSpace)
		strcat(buf, "\n\n");

	for (;;)
	{
		// print hex address
		sprintf(temp, "%08X  ", i);
		strcat(buf, temp);
		
		// print first 8 bytes
		for (int j = 0; j < 0x08; j++)
		{
			fChar = pAddr[dataidx];
			if(dataidx >= len) break;
			dataidx++;

			sprintf(temp, "%02X ", fChar);
			strcat(buf, temp);

			// add to the ASCII text
			line[bytesRead++] = GetChar(fChar);

			// this took three characters
			nSpaces -= 3;
		}

		// print last 8 bytes - change in the "xx " to " xx" provides
		// the double space in between the first 8 and the last 8 bytes.
		for (int j = 0x08; j < 0x10; j++)
		{
			fChar = pAddr[dataidx];
			if(dataidx >= len) break;
			dataidx++;

			sprintf(temp, " %02X", (unsigned char)fChar);
			strcat(buf, temp);

			// add to the ASCII text
			line[bytesRead++] = GetChar(fChar);

			// this took three characters
			nSpaces -= 3;
		}

		// fill in any leftover spaces.
		for (int j = 0; j <= nSpaces; j++)
		{
			strcat(buf, " ");
		}

		// print ASCII text
		sprintf(temp, "%s", line);
		strcat(buf, temp);

		// quit if the file is done
		if(dataidx >= len) break;

		// new line
		strcat(buf, "\n");

		if (dblSpace)
			strcat(buf,"\n");

		// reset everything
		bytesRead=0;
		memset(&line, 0, sizeof(line));
		i += 16;
		//dataidx++;
		nSpaces = 3*16+2;
	}

	return buf;
}

